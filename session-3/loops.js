// for loop
/* for (start value - best to init with let; 
        do loop while this is true for start var;
        change to start value) */

for (let i = 0; i <10; i+=2) {
    console.log('i = ', i);
};

const numbers = [1, 3, 7, 12];

for (let i = 0; i <numbers.length; i++) {
    const doubled = numbers[i]*2;
    console.log(doubled);
};

